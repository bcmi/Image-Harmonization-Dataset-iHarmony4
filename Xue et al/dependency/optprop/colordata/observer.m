	zwl=[];
	if nargin<2; check = false;end
	if nargin<1 || isempty(obs); obs=dcwf; end
	if ischar(obs)
		[z,zwl]=charobserver(cwf2obs(obs));
	elseif iscwf(obs)
		if ischar(obs.observer)
			[z,zwl]=charobserver(obs.observer);
		else
			z=obs.observer;
			zwl=obs.obswl;
			end
		end

